// Case-insensitive index over the canonical keys ("united kingdom" → "United Kingdom").
const SVG_COUNTRY_KEY_BY_LOWER = Object.keys(SVG_COUNTRY_COORDS)
  .reduce((acc, k) => { acc[k.toLowerCase()] = k; return acc; }, {});

function normalizeCountryName(name) {
  const raw = String(name || '').trim();
  if (SVG_COUNTRY_COORDS[raw]) return raw;
  const k = raw.toLowerCase().replace(/[.,]/g, '');
  return COUNTRY_NAME_ALIASES[k] || SVG_COUNTRY_KEY_BY_LOWER[k] || raw;
}

function svgCoordsFor(country) {
  return SVG_COUNTRY_COORDS[normalizeCountryName(country)] || null;
}

