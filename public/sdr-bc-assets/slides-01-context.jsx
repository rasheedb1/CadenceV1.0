// Tile w/ styled text fallback. `text: true` skips the image entirely;
// otherwise the image loads from c.src and falls back to text on error.
function TrustedMerchantTile({ c }) {
  const [failed, setFailed] = React.useState(c.text === true);
  return (
    <div className="card-nova" style={{
      display: 'flex', alignItems: 'center', justifyContent: 'center',
      padding: 14, minHeight: 0, overflow: 'hidden',
    }}>
      {failed || !c.src ? (
        <span style={{
          fontFamily: 'var(--font-display)', fontSize: 30,
          color: 'var(--ink)', lineHeight: 1,
          ...(c.style || {}),
        }}>{c.name}</span>
      ) : (
        <img
          src={c.src}
          alt={c.name}
          onError={() => setFailed(true)}
          style={{
            maxWidth: '68%', maxHeight: '50%',
            objectFit: 'contain',
            filter: 'brightness(0)',
            opacity: 0.88,
          }}
        />
      )}
    </div>
  );
}

